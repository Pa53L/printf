#include "../h_HEAD/header.h"

char	*is_accuracy(st_format_spec *spec, char *p)
{
	if (*(p + 1))
		p++;
	spec->accuracy = atoi(p);
	while (*p >= '0' && *p <= '9')
		p++;
	return (p);
}
