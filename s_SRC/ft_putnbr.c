#include "../h_HEAD/header.h"

void	ft_putnbr(long long int n)
{
	long long int i;

	if (n >= 0 && n <= 9)
	{
		i = n + '0';
		write(1, &i, 1);
		return ;
	}
	else if (n == -9223372036854775808)
	{
		write(1, "9223372036854775808", 19);
		return ;
	}
	else if (n < 0)
	{
		//write(1, "-", 1);
		ft_putnbr(n * -1);
	}
	else
	{
		ft_putnbr(n / 10);
		i = n % 10 + '0';
		write(1, &i, 1);
	}
}
